% Functions that Return gds_structure Objects 
% ===========================================
%
% gdsii_bitmap      - return the representation of a bitmap
%                     made from boundary element pixels.
% gdsii_datamatrix  - return a Datamatrix encoding of a 
%                     user supplied string.
% gdsii_cdfeature   - return a critical dimension pattern
%                     for lithography process quality control.       
% gdsii_grating     - return a linear grating pattern
% gdsii_replicate   - replicate a pattern described by a boundary
%                     element on a grid.
% gdsii_multref     - replicate structures at many positions; not
%                     restricted to a grid.
% gdsii_sierpinski  - return a Sierpinski fractal.
% gdsii_checky      - return a nested checker pattern for 
%                     lithography test exposures.
% gdsii_zonelens    - returns a layout consisting of concentric
%                     rings (e.g. a zone lens).
%
% Ulf Griesmann, NIST, 2008 - 2015
% -------------------------------------------------------

